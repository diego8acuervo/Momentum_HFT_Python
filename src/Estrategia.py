        # ─── SLOW LAYER (daily OLS via REST) ──────────────
        def _slow_layer_update(self):
            """
            Recompute structural OLS parameters using 1-day candles
            fetched via the Binance REST API.

            Option A (2026-04-27): CointegrationMonitor is the SOLE
            authority on pair selection. The live slow layer no longer
            gates entries on its own EG/ADF result — doing so with a
            90-day rolling window refreshed every 6 hours produced too
            many false positives that blocked profitable entries (the
            monitor selects on 180 days; the live gate fired when a
            single new daily bar temporarily failed the 90-day test).

            This method now:
              • Computes hedge_ratio, spread_mean, spread_std (OLS) —
                required for z-score normalisation.
              • Runs EG+ADF for DIAGNOSTIC/MONITORING purposes only.
                Results are stored in slow_params and visible in the
                dashboard but do NOT block entry signals.
              • Sets is_cointegrated=True whenever the OLS succeeds
                (valid spread and spread_std > 0). Backward-compatible
                with LiveMonitor and live_state_*.json readers.

            To re-enable the cointegration gate, add:
                if not coint: return   (after coint = ... line)
            and restore the block in calcular_senal_pares().
            """
            try:
                sym_y = self.par[0]
                sym_x = self.par[1]

                # Fetch daily OHLCV via REST
                # +1 so we can exclude the current (possibly incomplete)
                # bar from OLS, matching MRBacktest's causal framework.
                df_y = self.velas.get_perpetual_ohlcv(
                    sym_y,
                    interval='1d',
                    limit=self.SLOW_LAYER_WINDOW + 1,
                )
                df_x = self.velas.get_perpetual_ohlcv(
                    sym_x,
                    interval='1d',
                    limit=self.SLOW_LAYER_WINDOW + 1,
                )

                if df_y is None or df_x is None:
                    print("⚠️  Slow-layer: REST fetch returned None")
                    return
                if len(df_y) < 30 or len(df_x) < 30:
                    print(
                        f"⚠️  Slow-layer: not enough data "
                        f"(y={len(df_y)}, x={len(df_x)})"
                    )
                    return

                # Align lengths
                n = min(len(df_y), len(df_x))
                all_y = np.array(df_y['close'].astype(float).values[-n:])
                all_x = np.array(df_x['close'].astype(float).values[-n:])

                # Causal split: OLS on [:-1], bar [-1] reserved for
                # fast-layer z-score (out-of-sample, matching MRBacktest).
                if len(all_y) >= self.SLOW_LAYER_WINDOW + 1:
                    y = all_y[:-1]
                    x = all_x[:-1]
                else:
                    y = all_y
                    x = all_x

                if np.std(y) < 1e-8 or np.std(x) < 1e-8:
                    print("⚠️  Slow-layer: zero variance")
                    return

                # OLS hedge ratio on log prices (causal: excludes current bar)
                log_y = np.log(y)
                log_x = np.log(x)
                model  = sm.OLS(log_y, sm.add_constant(log_x))
                result = model.fit()
                hr     = result.params[-1]   # slope = hedge ratio

                # Spread & stats from OLS window only (causal: excludes current bar)
                spread  = log_y - hr * log_x
                sp_mean = np.mean(spread)
                sp_std  = np.std(spread)
                if sp_std < 1e-8:
                    print("⚠️  Slow-layer: spread std ≈ 0")
                    return

                # ── Diagnostic EG+ADF (NOT used as trading gate) ────────────
                # Retained for monitoring and dashboard visibility.
                # These tests are INFORMATIONAL ONLY — they do not block
                # entries. CointegrationMonitor is the gate authority.
                try:
                    _, eg_pval, _ = ts.coint(log_y, log_x)
                    adf = ts.adfuller(spread, maxlag=12)
                    adf_stat = adf[0]
                    adf_pval = adf[1]
                    crit_5   = adf[4]['5%']
                    adf_coint = (adf_stat <= crit_5) or (adf_pval <= 0.05)
                    coint_diag = (eg_pval <= 0.05) and adf_coint
                except Exception:
                    eg_pval = adf_stat = adf_pval = None
                    coint_diag = None

                # is_cointegrated = True whenever OLS succeeds (spread is valid).
                # The field is kept for backward compatibility with LiveMonitor
                # and live_state_*.json — it no longer gates trading logic.
                self.slow_params.update({
                    'hedge_ratio':    hr,
                    'spread_mean':    sp_mean,
                    'spread_std':     sp_std,
                    'is_cointegrated': True,        # OLS valid → ready to trade
                    'adf_stat':       adf_stat,
                    'adf_pvalue':     adf_pval,
                    'eg_pvalue':      eg_pval,
                    'last_update':    time.time(),
                })
                self._slow_layer_ready = True

                diag_tag = (
                    "✅" if coint_diag else
                    "⚠️ [diag only, not gating]" if coint_diag is not None else
                    "—"
                )
                print(
                    f"🔬 Slow-layer updated | HR={hr:.4f} "
                    f"| spread μ={sp_mean:.6f} σ={sp_std:.6f} "
                    f"| EG p={eg_pval:.4f} ADF={adf_stat:.3f} p={adf_pval:.4f} "
                    f"| coint_diag={diag_tag} [gate: CointegrationMonitor]"
                )

            except Exception as e:
                print(f"⚠️  Slow-layer error: {e}")
                import traceback
                traceback.print_exc()

        def sync_position_flags(self):
            """
            Synchronize largoMdo/cortoMdo flags with actual portfolio positions.
            
            This ensures strategy state matches reality, especially after:
            - Manual closes on exchange
            - Partial fills
            - System restarts
            - State drift over time
            
            SPREAD POSITION LOGIC:
            - LARGO spread = Long Y (par[0]) + Short X (par[1]) → pos_y > 0, pos_x < 0
            - CORTO spread = Short Y (par[0]) + Long X (par[1]) → pos_y < 0, pos_x > 0
            """
            if self.portafolio is None:
                return  # Portfolio not yet linked
            
            pos_y = self.portafolio.posiciones_actuales.get(self.par[0], 0)
            pos_x = self.portafolio.posiciones_actuales.get(self.par[1], 0)
            
            old_largo = self.largoMdo
            old_corto = self.cortoMdo
            
            # Determine spread position from actual holdings
            # LARGO spread: long Y (pos_y > 0), short X (pos_x < 0)
            # CORTO spread: short Y (pos_y < 0), long X (pos_x > 0)
            
            if pos_y > 0 and pos_x < 0:
                self.largoMdo = True
                self.cortoMdo = False
            elif pos_y < 0 and pos_x > 0:
                self.largoMdo = False
                self.cortoMdo = True
            elif pos_y == 0 and pos_x == 0:
                # Both flat = no spread position
                self.largoMdo = False
                self.cortoMdo = False
            else:
                # Mixed state (both positive, both negative, one zero) = uncertain
                # Keep current flags but log warning
                if pos_y != 0 or pos_x != 0:
                    print(f"⚠️  Position state unclear: {self.par[0]}={pos_y}, {self.par[1]}={pos_x}")
            
            # Log if state changed
            if old_largo != self.largoMdo or old_corto != self.cortoMdo:
                print(f"🔄 Position flags synced: largoMdo={old_largo}→{self.largoMdo}, cortoMdo={old_corto}→{self.cortoMdo}")
                print(f"   Actual positions: {self.par[0]}={pos_y}, {self.par[1]}={pos_x}")

