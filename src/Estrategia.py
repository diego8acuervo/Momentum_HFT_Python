        def calcular_senal_pares(self): 
            """
            Dual-timeframe signal engine:

            SLOW LAYER  – Daily candles via REST, refreshed every
            SLOW_LAYER_INTERVAL seconds.  Provides stable OLS
            hedge_ratio, spread mean/std for z-score normalisation.

            FAST LAYER  – Latest 1-min close prices from the
            WebSocket buffer.  Computes real-time spread and
            z-score using the slow-layer structural parameters.

            Pair selection authority: CointegrationMonitor (daily
            ranking).  The live slow layer no longer gates entries
            on its own EG/ADF result (Option A, 2026-04-27) — a
            90-day rolling test refreshed every 6 hours produced
            too many false positives.  Protection is provided by
            the z-score stop-loss in calcular_senalXY instead.
            """
            # ── 1. Refresh slow layer if stale ──────────────
            now = time.time()
            stale = (
                now - self.slow_params['last_update']
                > self.SLOW_LAYER_INTERVAL
            )
            if not self._slow_layer_first_done:
                # First refresh: apply random offset to stagger across pairs
                if now - self.slow_params['last_update'] < self._slow_layer_offset:
                    pass   # wait for offset before first refresh
                else:
                    self._slow_layer_update()
                    self._slow_layer_first_done = True
            elif stale or not self._slow_layer_ready:
                self._slow_layer_update()

            # Guard: OLS parameters must exist before computing z-score
            if not self._slow_layer_ready:
                print("⏳ Slow layer not ready – skipping signal")
                return

            hr = self.slow_params['hedge_ratio']
            sp_mean = self.slow_params['spread_mean']
            sp_std = self.slow_params['spread_std']

            # ── 2. Fast layer: real-time z-score ────────────
            # Use a short 1-min window (e.g. 60 bars = 1 hour)
            # just to get the latest close prices
            fast_window = min(self.ventanaOLS, 60)
            df_y = self.velas.get_valor_ultimas_velas(
                self.par[0], 'close', N=fast_window,
            )
            df_x = self.velas.get_valor_ultimas_velas(
                self.par[1], 'close', N=fast_window,
            )

            if df_y is None or df_x is None:
                return
            if len(df_y) == 0 or len(df_x) == 0:
                return

            # Current spread using slow-layer HR on LOG prices
            last_y = float(df_y[-1])
            last_x = float(df_x[-1])

            if last_y <= 0 or last_x <= 0:
                return

            spread_now = np.log(last_y) - hr * np.log(last_x)

            # Z-score normalised by slow-layer mean/std
            z_last = (spread_now - sp_mean) / sp_std

            # Also keep self.hedge_ratio so calcular_senalXY
            # uses the correct notional ratio
            self.hedge_ratio = hr

            print(
                f"📈 Fast z={z_last:.3f} | "
                f"spread={spread_now:.6f} | "
                f"μ={sp_mean:.6f} σ={sp_std:.6f} | "
                f"HR={hr:.4f}"
            )

            # ── 3. Generate signals via existing logic ──────
            senal_y, senal_x = self.calcular_senalXY(z_last)
            if senal_y is not None and senal_x is not None:
                self.eventos.put(senal_y)
                self.eventos.put(senal_x)
    
        def calcular_senales(self, evento):
            """
            Calcula las señales según los últimos datos de mercado.
            
            Implements SMART DEDUPLICATION to prevent processing the same candle
            multiple times (from WebSocket + Heartbeat sources).
            
            ✅ PAIR-COMPLETE GATE: Only evaluates signals when ALL symbols in the
            pair have received fresh data for this cycle. This prevents:
            - Double Z-score calculation with stale data on one leg
            - Immediate re-entry after exit due to Z-score flip between legs
            """

            if evento.type == 'MERCADO':
                # ✅ SMART DEDUPLICATION: Check if this candle was already processed
                nemo = evento.nemo
                # Get candle timestamp (prefer close_time, fallback to timestamp)
                candle_time = None
                if hasattr(evento, 'close_time') and evento.close_time is not None:
                    candle_time = evento.close_time
                elif hasattr(evento, 'timestamp') and evento.timestamp is not None:
                    candle_time = evento.timestamp
                elif hasattr(evento, 'open_time') and evento.open_time is not None:
                    candle_time = evento.open_time
                elif hasattr(evento, 'start_time') and evento.start_time is not None:
                    candle_time = evento.start_time
                elif hasattr(evento, 'end_time') and evento.end_time is not None:
                    candle_time = evento.end_time
                
                # Type validation - ensure candle_time is not a type/class
                if candle_time is not None and isinstance(candle_time, type):
                    print(f"⚠️ Invalid timestamp type detected for {nemo}: {candle_time}")
                    print(f"   Skipping this event (pair-complete gate requires valid timestamps)")
                    return
                
                if candle_time is None:
                    # No timestamp available, skip (pair-complete gate requires timestamps)
                    print(f"⚠️ No timestamp found for {nemo} evento, skipping")
                    return
                
                # Check if we've already processed this candle
                last_processed = self.last_processed_candle.get(nemo)
                
                if last_processed is not None and candle_time <= last_processed:
                    # This candle was already processed, skip to prevent duplicate signals
                    self._candle_dedup_count += 1
                    if self._candle_dedup_count % 10 == 0:  # Log every 10th duplicate
                        print(f"🔄 Skipped duplicate candle for {nemo} (#{self._candle_dedup_count} total)")
                        print(f"   Current: {candle_time}, Last processed: {last_processed}")
                    return
                
                # Update last processed timestamp
                self.last_processed_candle[nemo] = candle_time
                
                # Update self.datetime to the candle timestamp for signal creation
                self.datetime = candle_time
                
                # ✅ PAIR-COMPLETE GATE: Register this symbol as updated
                self._updated_symbols_this_cycle.add(nemo)
                self._last_cycle_candle_time[nemo] = candle_time
                
                # Check if ALL symbols in the pair have fresh data
                all_symbols_updated = all(
                    s in self._updated_symbols_this_cycle for s in self.lista_nemos
                )
                
                if not all_symbols_updated:
                    # Not all symbols have updated yet — wait for the rest
                    pending = [s for s in self.lista_nemos if s not in self._updated_symbols_this_cycle]
                    print(f"⏳ Pair-gate: {nemo} updated, waiting for {pending}")
                    return
                
                # ✅ ALL symbols have fresh data — evaluate signals ONCE
                print(f"✅ Pair-gate: All symbols updated {dict(self._last_cycle_candle_time)} → evaluating signals")
                
                # Reset the cycle tracker for the next heartbeat
                self._updated_symbols_this_cycle.clear()
                
                # Process the pair signal with consistent data
                self.calcular_senal_pares()
                

